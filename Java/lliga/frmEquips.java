     /**
     * Recupera les dades del formulari i les carrega en un objecte Equip
     */
    private Equip recuperarEquip() {
        Equip equip = new Equip();
        /*Caldria comprovar que les dades són correctes, idequip numèric,
        codi postal numèric de 5 xifres, etc*/
        equip.setEqId(Integer.parseInt(txtIdEquip.getText()));
        equip.setEqNom(txtNomEquip.getText());
        equip.setEstadi(txtEstadi.getText());
        equip.setPoblacio(txtPoblacio.getText());
        equip.setCodPostal(txtCodPostal.getText());
        return equip;
    }
    
    private void afegirEquip() {
        txtIdEquip.setText(Integer.toString(-1));
        Equip equip = recuperarEquip();
        Equips.afegirEquip(equip);
        Equips.obtenirEquips();
        Ultim();
    }
    
    private void modificarEquips() {
        Equip equip = recuperarEquip();
        if (llistaEquips.actual().compareTo(equip) != 0) {
            llistaEquips.updateEquip(equip);
        }
    }
    
