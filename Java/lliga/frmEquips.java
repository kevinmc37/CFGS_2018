    /**
     * Mostra el primer registre de la taula Equips
     */
    private void Primer(){
        /*Insereix aquí el codi per mostrar el primer registre,
        Ha de fer la crida a primer, i passar l'objecte Equip que
        retorna aquest mètode a la funció mostrarEquip(eq)*/
        Equip equip = llistaEquips.primer();
        mostrarEquip(equip);
    }

     /**
     * Mostra el registre anterior
     */
    private void Anterior(){
        /*Insereix aquí el codi per mostrar el primer registre*/
        Equip equip = llistaEquips.anterior();
        mostrarEquip(equip);
    }

     /**
     * Mostra el registre següent
     */
    private void Seguent(){
        /*Insereix aquí el codi per mostrar el primer registre*/
        Equip equip = llistaEquips.posterior();
        mostrarEquip(equip);
    }
     /**
     * Mostra el darrer registre
     */
    private void Ultim(){
        /*Insereix aquí el codi per mostrar el primer registre*/
        Equip equip = llistaEquips.ultim();
        mostrarEquip(equip);
    }
    
    private void Actual(){
        Equip equip = llistaEquips.actual();
        mostrarEquip(equip);
    }
    
    private void Afegir(){
        borrarDades();
        btnAcceptar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnPrimer.setEnabled(false);
        btnAnterior.setEnabled(false);
        btnSeguent.setEnabled(false);
        btnUltim.setEnabled(false);
        btnAfegir.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnMostrar.setEnabled(false);
    }
    
    private void Eliminar(){
        int comprovacio = javax.swing.JOptionPane.showConfirmDialog(null, "Vols eliminar el registre?", "Eliminar registre", javax.swing.JOptionPane.YES_NO_OPTION);
        if (comprovacio == 0) {
            llistaEquips.eliminarEquip();
            Actual();
        }
    }

     /**
     * Mostra el registre anterior
     * @param equip Dades de l'objecte equip a mostrar
     */
    private void mostrarEquip(Equip equip){
        if (equip != null) {
            txtNomEquip.setEditable(true);
            txtEstadi.setEditable(true);
            txtPoblacio.setEditable(true);
            txtCodPostal.setEditable(true);
            btnEliminar.setEnabled(true);
            txtIdEquip.setText(Integer.toString(equip.getEqId()));
            txtNomEquip.setText(equip.getEqNom());
            txtEstadi.setText(equip.getEstadi());
            txtPoblacio.setText(equip.getPoblacio());
            txtCodPostal.setText(equip.getCodPostal());
            actualitzarBotons();
        }
        else {
            txtNomEquip.setEditable(false);
            txtEstadi.setEditable(false);
            txtPoblacio.setEditable(false);
            txtCodPostal.setEditable(false);
            btnEliminar.setEnabled(false);
            btnPrimer.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnSeguent.setEnabled(false);
            btnUltim.setEnabled(false);
        }
    }
    
    private void mostrarJugadors() {
        frmJugadors jugadors = new frmJugadors();
        jugadors.idEquip = Integer.parseInt(txtIdEquip.getText());
        jugadors.getLlistaJugadors();
        jugadors.setVisible(true);
    }
    
    private void borrarDades() {
        txtIdEquip.setText("");
        txtNomEquip.setText("");
        txtEstadi.setText("");
        txtPoblacio.setText("");
        txtCodPostal.setText("");
    }
    
     /**
     * Activa o desactiva els botons en funció de si estem en el primer o darrer
     * registre
     * @param pucEnrere true si puc tirar enrere, false en cas contrari
     * @param pucEndavant true si puc tirar endavant, false en cas contrari
     */
